#include<iostream>

using namespace std;

int main()
{
    int a;
    a=12;
    cout<<"Size of int "<<sizeof(a)<<endl;

    float b;
    cout<<"size of float "<<sizeof(b)<<endl;

    bool c;
    cout<<"size of bool "<<sizeof(c)<<endl;

    long d;
    cout<<"size of long "<<sizeof(d)<<endl;

    double e;
    cout<<"size of double "<<sizeof(e)<<endl;

    char f;
    cout<<"size of char "<<sizeof(f)<<endl;


    short g;
    cout<<"size of short "<<sizeof(g)<<endl;


return 0;

}